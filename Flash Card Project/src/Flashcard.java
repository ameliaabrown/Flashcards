
public class Flashcard {
	
	private String front;
	private String back;
	
	public Flashcard (String f, String b)
	{
		front = f;
		back = b;
	}

	public String getFront() {
		return front;
	}

	public void setFront(String front) {
		this.front = front;
	}

	public String getBack() {
		return back;
	}

	public void setBack(String back) {
		this.back = back;
	}

	


}
